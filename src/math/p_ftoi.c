#include <pal.h>

/**
 *
 * Converts a floating point values in 'a' to signed integer values.
 *
 * @param a     Pointer to input vector
 *
 * @param c     Pointer to output vector
 *
 * @param n     Size of 'a' and 'c' vector.
 *
 * @return      None
 *
 */
void p_ftoi(float *a, int *c, int n) {
    int i;
    for(i=0; i<n; i++)
	*c++ = (int) *a++;
}
